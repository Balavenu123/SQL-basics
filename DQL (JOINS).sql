/* ANSI JOINS -> ON CONDITION */

INNER_JOIN:
-----------

SELECT *FROM EMPLOYEE_2024 INNER JOIN EMPLOYEE_2025 ON EMPLOYEE_2024.EMPID = EMPLOYEE_2025.EMPID

OUTER_JOIN:
-----------
SELECT *FROM EMPLOYEE_2024 LEFT OUTER JOIN EMPLOYEE_2025 ON EMPLOYEE_2024.EMPID = EMPLOYEE_2025.EMPID

SELECT *FROM EMPLOYEE_2024 RIGHT OUTER JOIN EMPLOYEE_2025 ON EMPLOYEE_2024.EMPID = EMPLOYEE_2025.EMPID

