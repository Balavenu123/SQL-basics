SELECT *FROM EMPLOYEE_2024

CROSS_JOIN:
-----------
SELECT * FROM EMPLOYEE_2024 CROSS JOIN EMPLOYEE_2025

EQUI_JOIN:
-----------

SELECT *FROM EMPLOYEE_2024,EMPLOYEE_2025 WHERE EMPLOYEE_2024.EMPID = EMPLOYEE_2025.EMPID

NON_EQUI_JOIN:
---------------

SELECT *FROM EMPLOYEE_2024,EMPLOYEE_2025 WHERE EMPLOYEE_2024.EMPID > EMPLOYEE_2025.EMPID

